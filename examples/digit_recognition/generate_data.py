def _shift_image(img, dx, dy):
    """平移图像"""
    result = np.zeros_like(img)
    h, w = img.shape
    for y in range(h):
        for x in range(w):
            nx, ny = x + dx, y + dy
            if 0 <= nx < w and 0 <= ny < h:
                result[ny, nx] = img[y, x]
    return result


def _augment_template(template):
    """对模板进行数据增强：平移 + 噪声 + 亮度变化"""
    img = template.copy()
    # 随机平移 (-1, 0, 1)
    dx = np.random.randint(-1, 2)
    dy = np.random.randint(-1, 2)
    img = _shift_image(img, dx, dy)
    # 亮度抖动
    img = img * np.random.uniform(0.7, 1.0)
    # 高斯噪声
    img += np.random.randn(*img.shape) * 0.1
    return np.clip(img, 0.0, 1.0)


def generate_digit_dataset(n_per_digit=200, img_size=10):
    """
    生成合成数字数据集
    返回: X (n_samples, img_size^2), y (n_samples,)
    """
    X, y = [], []
    for digit in range(10):
        template = DIGIT_TEMPLATES[digit]
        for _ in range(n_per_digit):
            augmented = _augment_template(template)
            X.append(augmented.flatten())
            y.append(digit)

    X = np.array(X, dtype=np.float32)
    y = np.array(y, dtype=np.int64)
    # 打乱
    idx = np.random.permutation(len(y))
    return X[idx], y[idx]


